package Assignment17;

public class RelationalOperators {
	// Main method
	public static void main(String[] args) {
		// Initializing variable
		int a = 20, b = 15;
		System.out.println("== is the equality operator: " + (a == b));
		System.out.println("!= is for non-equality operator: " + (a != b));
		System.out.println("< is less than operator: " + (a < b));
		System.out.println("> is greater than operator: " + (a > b));
		System.out.println("<= is less than or equal to operator: " + (a <= b));
		System.out.println(">= is greater than or equal to operator: " + (a >= b));
	}

}
